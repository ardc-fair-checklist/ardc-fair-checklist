import { describe } from 'vitest'
import { expect } from 'vitest'
import { test } from 'vitest'

describe('State', () => {
    describe('If one element of the compliance state is incremented from code,', () => {
        test.todo('the progress percentage for the corresponding aspect should go up', () => {
            expect(true).toBe(true);
        })
        test.todo('the progress percentage for the corresponding aspect should go up by the correct amount', () => {
            expect(true).toBe(true);
        })
        test.todo('the overall progress percentage should go up', () => {
            expect(true).toBe(true);
        })
        test.todo('the overall progress percentage should go up by the correct amount', () => {
            expect(true).toBe(true);
        })
        test.todo('the newly selected answer on the incremented question should get the \'selected\' state', () => {
            expect(true).toBe(true);
        })
        test.todo('all other answers to that question should not have the \'selected\' state', () => {
            expect(true).toBe(true);
        })
    })
    describe('If one element of the compliance state is decremented from code,', () => {
        test.todo('the progress percentage for the corresponding aspect should go down', () => {
            expect(true).toBe(true);
        })
        test.todo('the progress percentage for the corresponding aspect should go down by the correct amount', () => {
            expect(true).toBe(true);
        })
        test.todo('the overall progress percentage should go down', () => {
            expect(true).toBe(true);
        })
        test.todo('the overall progress percentage should go down by the correct amount', () => {
            expect(true).toBe(true);
        })
        test.todo('the newly selected answer on the decremented question should get the \'selected\' state', () => {
            expect(true).toBe(true);
        })
        test.todo('all other answers to that question should not have the \'selected\' state', () => {
            expect(true).toBe(true);
        })
    })
})
